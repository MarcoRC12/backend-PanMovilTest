                        $data = array(
                            "Status"=>200,
                            "Total de registros"=>count($Permisos), 
                            "Detalle"=>$Permisos);
                    }
                    else{
                        $data = array(
                            "Status"=>404,
                            "Total de registros"=>0, 
                            "Detalle"=>"No hay registros");
                    }
                    return json_encode($data, true);
                }
                else{
                    $data = array(
                        "Status"=>404,
                        "Detalles"=>"El token es incorrecto"
                    );
                }
            }
            else{
                $data = array(
                    "Status"=>404,
                    "Detalles"=>"No posee autorizaci贸n"
                );
            }
        }
        return json_encode($data, true);        
    }
